package com.dicoding.academy.programminglanguage;

public class Programming {

    private String languageName;
    private String languageContent;
    private String languageAuthor;
    private String languageAuthorName;
    private String languageSince;
    private String languageSinceNumber;
    private String languageTags;
    private String languageTagsTitle;
    private int languageImage;

    public String getLanguageName() {
        return languageName;
    }

    public void setLanguageName(String languageName) {
        this.languageName = languageName;
    }

    public String getLanguageContent() {
        return languageContent;
    }

    public void setLanguageContent(String languageContent) {
        this.languageContent = languageContent;
    }

    public String getLanguageAuthor() {
        return languageAuthor;
    }

    public void setLanguageAuthor(String languageAuthor) {
        this.languageAuthor = languageAuthor;
    }

    public String getLanguageAuthorName() {
        return languageAuthorName;
    }

    public void setLanguageAuthorName(String languageAuthorName) {
        this.languageAuthorName = languageAuthorName;
    }

    public String getLanguageSince() {
        return languageSince;
    }

    public void setLanguageSince(String languageSince) {
        this.languageSince = languageSince;
    }

    public String getLanguageSinceNumber() {
        return languageSinceNumber;
    }

    public void setLanguageSinceNumber(String languageSinceNumber) {
        this.languageSinceNumber = languageSinceNumber;
    }

    public String getLanguageTags() {
        return languageTags;
    }

    public void setLanguageTags(String languageTags) {
        this.languageTags = languageTags;
    }

    public String getLanguageTagsTitle() {
        return languageTagsTitle;
    }

    public void setLanguageTagsTitle(String languageTagsTitle) {
        this.languageTagsTitle = languageTagsTitle;
    }

    public int getLanguageImage() {
        return languageImage;
    }

    public void setLanguageImage(int languageImage) {
        this.languageImage = languageImage;
    }
}
